package com.java;
/*WAP to display that the number is even or odd number*/
public class EvenOdd {
	public static void main(String args[]) {
		int num=19;
		if(num % 2 ==0 ) {
			System.out.println( num+ " It is an even number.");}
		else 
		{
			System.out.println( num+ " It is an odd number.");
		}
		
	} 
}
